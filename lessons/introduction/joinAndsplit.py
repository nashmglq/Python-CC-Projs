# Join is use to join statements
# syntax = string.join(iterable)
def joinFunc():
    uses = ["h","q","HEY"]

    print(" ".join(uses))
    
#Split or split() is to split a string to a list
# syntax = var.split() or var.split("#") if you want to split it in certain char
def splitFunc():
    strs = "Hey everyone!"
    print(strs.split())
    
joinFunc()
splitFunc()


