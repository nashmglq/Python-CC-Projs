#For loops, for (named you given) in (who you looping)

fruits = ["apple", "banana", "cherry"]
for x in fruits:
  #x is the content being target each iteration
  print(x)

#For loops for range, range(start, stop, step)

for x in range(2, 30, 3):
  print(x)


#While loop in python, do the condition while it is true
i = 1

while i < 6:
  print(i)
  i += 1



